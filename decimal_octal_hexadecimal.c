#include <stdio.h>
int main(){
    int decimalNum;
    printf("enter decimal number: ");
    scanf("%d",&decimalNum);
    
    printf("decimal: %d\n",decimalNum);
    printf("octal: %o\n",decimalNum);
    printf("hexadecimal: %x\n",decimalNum);
    printf("hexadecimal: %X\n",decimalNum);
    return 0;


}
